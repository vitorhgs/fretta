/**
 * Retorna o estilo baseado na hora do dia
 * 6h às 18h: claro
 * 18h às 6h: escuro
 */
export function estiloAutomatico(): MapStyle {
  const hora = new Date().getHours();
  if (hora >= 6 && hora < 18) return mapaClaro;
  return mapaEscuro;
}